# if 结构
age = 4
if age:
    print('your age is', age)

# if-else 结构
age = 3
# : 冒号后缩进表示代码块
if age >= 18:
    print('your age is', age)
    print('adult')
else:
    print('your age is', age)
    print('children')

# if-elif-else 结构
age = 20
if age >= 6:
    print('teenager')
elif age >= 18:
    print('adult')
else:
    print('kid')

